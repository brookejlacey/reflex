"""
Jinja2-based postmortem report generator for Reflex.

Takes structured data from all six agents in the pipeline and generates
a formatted Markdown postmortem report. Outputs both a standalone file
and a GitLab issue comment-ready format.
"""

from __future__ import annotations

import logging
import os
from datetime import datetime, timezone
from pathlib import Path
from typing import Any, Optional

logger = logging.getLogger(__name__)

try:
    from jinja2 import Environment, BaseLoader
except ImportError:
    Environment = None  # type: ignore[assignment,misc]
    BaseLoader = None  # type: ignore[assignment,misc]
    logger.warning("jinja2 not installed. Install it with: pip install jinja2")


# ---------------------------------------------------------------------------
# Jinja2 template for the Markdown postmortem
# ---------------------------------------------------------------------------

