def _commaformat(value: Any) -> str:
    """Jinja2 filter: format a number with comma separators."""
    try:
        return f"{int(value):,}"
    except (ValueError, TypeError):
        return str(value)


class ReportGenerator:
    """
    Generates formatted Markdown postmortem reports from structured agent data.

    Uses Jinja2 templates for flexible rendering. Falls back to basic string
    formatting if Jinja2 is not installed.
    """

    def __init__(self, output_dir: Optional[str] = None) -> None:
        """
        Args:
            output_dir: Directory to write report files. Defaults to ./reports/.
        """
        self.output_dir = Path(output_dir) if output_dir else Path("reports")

        if Environment is not None and BaseLoader is not None:
            self._env = Environment(loader=BaseLoader())
            self._env.filters["commaformat"] = _commaformat
            self._postmortem_tpl = self._env.from_string(POSTMORTEM_TEMPLATE)
            self._comment_tpl = self._env.from_string(GITLAB_COMMENT_TEMPLATE)
        else:
            self._env = None
            self._postmortem_tpl = None
            self._comment_tpl = None

    def generate(
        self,
        incident_id: str,
        triage: Optional[dict[str, Any]] = None,
        diagnostics: Optional[dict[str, Any]] = None,
        root_cause: Optional[dict[str, Any]] = None,
        fix: Optional[dict[str, Any]] = None,
        validation: Optional[dict[str, Any]] = None,
        sustainability: Optional[dict[str, Any]] = None,
        timeline: Optional[list[dict[str, str]]] = None,
        severity: str = "Unknown",
        status: str = "Resolved",
        recommendations: Optional[list[str]] = None,
    ) -> str:
        """
        Generate the full Markdown postmortem report.

        Args:
            incident_id: Unique identifier for the incident.
            triage: Structured output from the triage agent.
            diagnostics: Structured output from the diagnostics agent.
            root_cause: Structured output from the root cause analysis agent.
            fix: Structured output from the fix agent.
            validation: Structured output from the validation agent.
            sustainability: Output from CarbonTracker.generate_report().
            timeline: List of {"time": ..., "description": ...} events.
            severity: Incident severity label.
            status: Current incident status.
            recommendations: List of follow-up recommendations.

        Returns:
            The rendered Markdown report as a string.
        """
        context = self._build_context(
            incident_id=incident_id,
            triage=triage,
            diagnostics=diagnostics,
            root_cause=root_cause,
            fix=fix,
            validation=validation,
            sustainability=sustainability,
            timeline=timeline,
            severity=severity,
            status=status,
            recommendations=recommendations,
        )

        if self._postmortem_tpl is not None:
            return self._postmortem_tpl.render(**context)

        return self._fallback_render(context)

    def generate_gitlab_comment(
        self,
        incident_id: str,
        triage: Optional[dict[str, Any]] = None,
        diagnostics: Optional[dict[str, Any]] = None,
        root_cause: Optional[dict[str, Any]] = None,
        fix: Optional[dict[str, Any]] = None,
        validation: Optional[dict[str, Any]] = None,
        sustainability: Optional[dict[str, Any]] = None,
        timeline: Optional[list[dict[str, str]]] = None,
        severity: str = "Unknown",
        status: str = "Resolved",
        recommendations: Optional[list[str]] = None,
    ) -> str:
        """
        Generate a compact report formatted for a GitLab issue comment.

        Same parameters as generate(). Returns a shorter Markdown string.
        """
        context = self._build_context(
            incident_id=incident_id,
            triage=triage,
            diagnostics=diagnostics,
            root_cause=root_cause,
            fix=fix,
            validation=validation,
            sustainability=sustainability,
            timeline=timeline,
            severity=severity,
            status=status,
            recommendations=recommendations,
        )

        if self._comment_tpl is not None:
            return self._comment_tpl.render(**context)

        return self._fallback_render(context, compact=True)

    def write_report(
        self,
        report_content: str,
        incident_id: str,
        suffix: str = "",
    ) -> str:
        """
        Write a report to a file.

        Args:
            report_content: The rendered Markdown content.
            incident_id: Used to name the file.
            suffix: Optional suffix before the file extension.

        Returns:
            The absolute path to the written file.
        """
        self.output_dir.mkdir(parents=True, exist_ok=True)
        safe_id = incident_id.replace("/", "_").replace(" ", "_")
        filename = f"postmortem_{safe_id}{suffix}.md"
        filepath = self.output_dir / filename

        filepath.write_text(report_content, encoding="utf-8")
        logger.info("Report written to %s", filepath.resolve())
        return str(filepath.resolve())

    @staticmethod
    def _build_context(
        incident_id: str,
        triage: Optional[dict[str, Any]] = None,
        diagnostics: Optional[dict[str, Any]] = None,
        root_cause: Optional[dict[str, Any]] = None,
        fix: Optional[dict[str, Any]] = None,
        validation: Optional[dict[str, Any]] = None,
        sustainability: Optional[dict[str, Any]] = None,
        timeline: Optional[list[dict[str, str]]] = None,
        severity: str = "Unknown",
        status: str = "Resolved",
        recommendations: Optional[list[str]] = None,
    ) -> dict[str, Any]:
        """Assemble the Jinja2 template context dict."""
        return {
            "incident_id": incident_id,
            "generated_at": datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M:%S UTC"),
            "severity": severity,
            "status": status,
            "timeline": timeline or [],
            "triage": triage or {},
            "diagnostics": diagnostics or {},
            "root_cause": root_cause or {},
            "fix": fix or {},
            "validation": validation or {},
            "sustainability": sustainability,
            "recommendations": recommendations or [],
        }

    @staticmethod
    def _fallback_render(context: dict[str, Any], compact: bool = False) -> str:
        """
        Basic string-based rendering when Jinja2 is not available.

        Produces a simplified but readable Markdown report.
        """
        lines = [
            f"# Incident Postmortem: {context['incident_id']}",
            "",
            f"**Generated:** {context['generated_at']}",
            f"**Severity:** {context['severity']} | **Status:** {context['status']}",
            "",
        ]

        if not compact:
            lines.append("## Timeline")
            lines.append("")
            for event in context.get("timeline", []):
                lines.append(f"- **{event.get('time', '?')}** {event.get('description', '')}")
            lines.append("")

        # Root cause
        rc = context.get("root_cause", {})
        lines.append("## Root Cause")
        lines.append(rc.get("summary", "Analysis pending."))
        lines.append("")

        # Fix
        fix = context.get("fix", {})
        lines.append("## Fix Applied")
        lines.append(fix.get("summary", "Fix pending."))
        lines.append("")

        # Sustainability
        sus = context.get("sustainability")
        if sus:
            comp = sus.get("comparison", {})
            lines.append("## Sustainability")
            lines.append(
                f"Carbon saved: {comp.get('carbon_saved_grams_co2', 0):.2f} g CO2 "
                f"({comp.get('carbon_reduction_percent', 0):.1f}% reduction)"
            )
            lines.append("")

        lines.append("---")
        lines.append("*Generated by Reflex autonomous pipeline.*")

        return "\n".join(lines)
